def produzione_latte(nr_mucche, resa_giornaliera, giorni_simulazione):
    litri_al_giorno = nr_mucche * resa_giornaliera
    totale = litri_al_giorno * giorni_simulazione
    return totale


def produzione_carne(nr_mucche, gg_ingrasso, giorni_simulazione,
                     peso_iniziale=200, peso_finale=600):
    kg_per_capo = peso_finale - peso_iniziale
    cicli = giorni_simulazione / gg_ingrasso
    capi_completati = nr_mucche * cicli
    kg_totali = capi_completati * kg_per_capo
    return round(kg_totali, 2)


def produzione_fieno(nr_ettari, resa, giorni_simulazione):
    kg_fieno_prodotto = nr_ettari * resa * (giorni_simulazione / 365)
    return kg_fieno_prodotto


def calcolo_autosufficienza_mangime(kg_fieno_prodotto, nr_mucche_latte,
                                    consumo_fieno_per_mucca_latte,
                                    nr_mucche_carne, consumo_fieno_per_mucca_carne,
                                    giorni_simulazione):
    fabbisogno_latte = nr_mucche_latte * consumo_fieno_per_mucca_latte * giorni_simulazione
    fabbisogno_carne = nr_mucche_carne * consumo_fieno_per_mucca_carne * giorni_simulazione
    fabbisogno_totale = fabbisogno_latte + fabbisogno_carne
    surplus_deficit = kg_fieno_prodotto - fabbisogno_totale
    return fabbisogno_totale, surplus_deficit


def costo_azienda(nr_mucche_totali, nr_ettari, params, giorni_simulazione):
    costo_acq_animali = (nr_mucche_totali
                         * params["acqua_animali_giornaliera_media_litri"] / 1000
                         * giorni_simulazione
                         * costo_acqua)

    costo_acq_campi = (nr_ettari
                       * params["acqua_irrigazione_ettaro_m3_anno"]
                       * (giorni_simulazione / 365)
                       * costo_acqua)

    costo_fert = (nr_ettari
                  * params["fertilizzanti_ettaro_kg_anno"]
                  * (giorni_simulazione / 365)
                  * costo_fertilizzante)

    return round(costo_acq_animali + costo_acq_campi + costo_fert, 2)


def tempo_produzione(nr_mucche_latte, resa_giornaliera_latte,
                     gg_ingrasso,
                     nr_ettari, resa_ettaro_fieno, capacita_raccolta_giornaliera_kg):

    prod_latte_litri = produzione_latte(nr_mucche_latte, resa_giornaliera_latte, 365)
    produzione_giornaliera_latte = nr_mucche_latte * resa_giornaliera_latte
    gg_latte = prod_latte_litri / produzione_giornaliera_latte

    gg_carne = gg_ingrasso

    kg_fieno_totale = nr_ettari * resa_ettaro_fieno
    gg_fieno = kg_fieno_totale / capacita_raccolta_giornaliera_kg

    return {
        "gg_per_target_latte": round(gg_latte, 1),
        "gg_ciclo_carne"     : round(gg_carne, 1),
        "gg_raccolta_fieno"  : round(gg_fieno, 1),
        "gg_totale_lotto"    : round(max(gg_latte, gg_carne, gg_fieno), 1)
    }


