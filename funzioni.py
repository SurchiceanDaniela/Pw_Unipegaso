def genera_grafici_confronto(r1, r2):
    labels = [r1['tipo'], r2['tipo']]

    produzioni_latte = [r1['litri_latte'], r2['litri_latte']]
    costi_totali = [r1['costo_totale'], r2['costo_totale']]
    tempi_lotto = [r1['tempi']['gg_totale_lotto'], r2['tempi']['gg_totale_lotto']]

    fig, ax = plt.subplots(1, 3, figsize=(18, 5))
    fig.suptitle('Analisi Comparativa Processi Produttivi', fontsize=16)

    ax[0].bar(labels, produzioni_latte, color=['#4CAF50', '#F44336'])
    ax[0].set_title('Volume Latte Prodotto (L)')
    ax[0].set_ylabel('Litri')

    ax[1].bar(labels, costi_totali, color=['#81C784', '#E57373'])
    ax[1].set_title('Costi Operativi Totali (€)')
    ax[1].set_ylabel('Euro')

    ax[2].bar(labels, tempi_lotto, color=['#2E7D32', '#C62828'])
    ax[2].set_title('Tempo Completamento Lotto (GG)')
    ax[2].set_ylabel('Giorni')

    plt.tight_layout(rect=[0, 0.03, 1, 0.95])
    plt.show()


def salva_report_pdf(r1, r2, nome_file="report_azienda.pdf"):

    c = canvas.Canvas(nome_file, pagesize=A4)
    larghezza, altezza = A4
    y = altezza - 50

    def scrivi(testo, dimensione=11, grassetto=False, spazio=18):
        nonlocal y
        if grassetto:
            c.setFont("Helvetica-Bold", dimensione)
        else:
            c.setFont("Helvetica", dimensione)
        c.drawString(50, y, testo)
        y -= spazio

    def scrivi_riga(etichetta, val1, val2):
        nonlocal y
        c.setFont("Helvetica", 10)
        c.drawString(50,  y, etichetta)
        c.drawString(350, y, str(val1))
        c.drawString(470, y, str(val2))
        y -= 16

    def linea():
        nonlocal y
        c.line(50, y, larghezza - 50, y)
        y -= 10

    scrivi("REPORT DI SIMULAZIONE - AZIENDA ZOOTECNICA", dimensione=14, grassetto=True, spazio=10)
    scrivi(f"Durata simulazione: {gg_simulazione} giorni", dimensione=10, spazio=6)
    linea()

    scrivi_riga("", r1["tipo"], r2["tipo"])
    linea()

    scrivi("Parametri input", grassetto=True, spazio=14)
    scrivi_riga("Mucche da latte",  r1["nr_mucche_latte"], r2["nr_mucche_latte"])
    scrivi_riga("Mucche da carne",  r1["nr_mucche_carne"], r2["nr_mucche_carne"])
    scrivi_riga("Ettari coltivati", r1["nr_ettari"],       r2["nr_ettari"])
    y -= 6
    linea()

    scrivi("Produzioni", grassetto=True, spazio=14)
    scrivi_riga("Latte prodotto (L)",  f"{r1['litri_latte']:.2f}",  f"{r2['litri_latte']:.2f}")
    scrivi_riga("Carne prodotta (kg)", f"{r1['kg_carne']:.2f}",     f"{r2['kg_carne']:.2f}")
    scrivi_riga("Fieno prodotto (kg)", f"{r1['kg_fieno']:.2f}",     f"{r2['kg_fieno']:.2f}")
    y -= 6
    linea()

    scrivi("Gestione mangime", grassetto=True, spazio=14)
    scrivi_riga("Fabbisogno fieno (kg)", f"{r1['fabbisogno_fieno']:.2f}", f"{r2['fabbisogno_fieno']:.2f}")
    scrivi_riga("Surplus/deficit (kg)",  f"{r1['surplus_deficit']:.2f}",  f"{r2['surplus_deficit']:.2f}")
    y -= 6
    linea()

    scrivi("Costi", grassetto=True, spazio=14)
    scrivi_riga("Costo totale (euro)", f"{r1['costo_totale']:.2f}", f"{r2['costo_totale']:.2f}")
    y -= 6
    linea()

    scrivi("Tempi di produzione (giorni)", grassetto=True, spazio=14)
    scrivi_riga("Giorni target latte",   f"{r1['tempi']['gg_per_target_latte']:.1f}", f"{r2['tempi']['gg_per_target_latte']:.1f}")
    scrivi_riga("Giorni ciclo carne",    f"{r1['tempi']['gg_ciclo_carne']:.1f}",      f"{r2['tempi']['gg_ciclo_carne']:.1f}")
    scrivi_riga("Giorni raccolta fieno", f"{r1['tempi']['gg_raccolta_fieno']:.1f}",   f"{r2['tempi']['gg_raccolta_fieno']:.1f}")
    scrivi_riga("Tempo totale lotto",    f"{r1['tempi']['gg_totale_lotto']:.1f}",     f"{r2['tempi']['gg_totale_lotto']:.1f}")
    y -= 6
    linea()

    c.save()
    print(f"Report salvato come '{nome_file}'")
